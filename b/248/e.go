package main

import(
	"bufio"
	"fmt"
	"math"
	"os"
	"strconv"
)

func main() {
	defer iou.Fl()

	n := iou.I()
	k := iou.I()
	x, y := iou.Is2(n)

	if k == 1 {
		iou.Pl("Infinity")
		return
	}

	ans := 0
	var done [300][300]bool
	for i:=0; i<n; i++ {
		for j:=i+1; j<n; j++ {
			c := 2
			
			if done[i][j] {
				continue
			}

			pass := make([]int, 2)
			pass[0] = i
			pass[1] = j
			for l:=0; l<n; l++ {
				if l == i || l == j {
					continue
				}

				if done[i][l] {
					continue
				}

				//3点目通るか判定
				if (x[j] - x[i]) * (y[l] - y[i]) != (y[j] - y[i]) * (x[l] - x[i]) {
					continue
				}

				pass = append(pass, l)
				c++
			}

			for ii:=0; ii<c; ii++ {
				for jj:=ii+1; jj<c; jj++ {
					done[pass[ii]][pass[jj]] = true
				}
			}

			if c >= k {
				ans++
			}
		}
	}

	iou.Pl(ans)
}

